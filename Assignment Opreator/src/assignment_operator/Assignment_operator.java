package assignment_operator;

public class Assignment_operator 
{

	public static void main(String[] args) 
	{
		int a=40 , b=20;
		//"="is simple assignment operator
		
		System.out.println("value of a =" +a);
		System.out.println("value of b =" +b);
		
		//use of compound assignment operator "+= , -= , *= , /= " 
		//use compound  addition assignment operator "+="
		
		a += b; //a = a + b
		
		System.out.println("New value of a =" +a);
		
		// value of "a" change because of use above condition
		
		// use compound subtraction assignment operator "-="
		
		a -= b; // a = a - b
		
		System.out.println("New Value of a =" +a);
		
		//value of "a" changed again because of using assignment operator
		
		// use compound multiplication assignment operator "*="
		
		a *= b; // a = a * b
		
		System.out.println("New Value of a =" +a);
		
		//value of "a" changed again because of using assignment operator
		
		// use compound division assignment operator
		
		a /= b; // a = a / b
		
		System.out.println("New value of a =" +a);
		
		//value of "a" changed again because of using assignment operator
		
		
		
		
		
		
		
				
		
		

	}

}
